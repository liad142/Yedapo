const DEFAULT_STRINGS = TRANSLATIONS.en;

/** Get region info for a country code. Falls back to US/English. */
export function getRegion(countryCode: string): { lang: string; locale: string } {
  const r = REGION_CONFIG[countryCode.toUpperCase()];
  return r ? { lang: r.lang, locale: r.locale } : { lang: DEFAULT_REGION.lang, locale: DEFAULT_REGION.locale };
}

/** Get translated UI strings for a language code. Falls back to English. */
export function getStrings(lang: string): UIStrings {
  return TRANSLATIONS[lang] || DEFAULT_STRINGS;
}

/** Get accepted podcast languages for a country (used by daily-mix filtering). Falls back to null. */
export function getCountryLanguages(countryCode: string): string[] | null {
  const r = REGION_CONFIG[countryCode.toUpperCase()];
  return r ? r.languages : null;
}

export type { UIStrings };
